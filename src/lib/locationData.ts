export const getStatesForCountry = (countryCode: string) => {
  return statesByCountry[countryCode] || [];
};

export const getCountryName = (code: string) => {
  return countries.find(c => c.code === code)?.name || code;
};

export const getStateName = (countryCode: string, stateCode: string) => {
  const states = statesByCountry[countryCode];
  return states?.find(s => s.code === stateCode)?.name || stateCode;
};
